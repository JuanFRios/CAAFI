package co.com.caafi.model;

public class StudentStatistics {

	private int semestre;
	private int count;
	private int desertCount;
	private int perpruebaCount;
	private int insufiCount;
	private int newCount;
	private int cancCount;
	private int degreesCount;
	private double promNrosem;
	
	public int getSemestre() {
		return semestre;
	}
	public void setSemestre(int semestre) {
		this.semestre = semestre;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getDesertCount() {
		return desertCount;
	}
	public void setDesertCount(int desertCount) {
		this.desertCount = desertCount;
	}
	public int getPerpruebaCount() {
		return perpruebaCount;
	}
	public void setPerpruebaCount(int perpruebaCount) {
		this.perpruebaCount = perpruebaCount;
	}
	public int getInsufiCount() {
		return insufiCount;
	}
	public void setInsufiCount(int insufiCount) {
		this.insufiCount = insufiCount;
	}
	public int getNewCount() {
		return newCount;
	}
	public void setNewCount(int newCount) {
		this.newCount = newCount;
	}
	public int getCancCount() {
		return cancCount;
	}
	public void setCancCount(int cancCount) {
		this.cancCount = cancCount;
	}
	public int getDegreesCount() {
		return degreesCount;
	}
	public void setDegreesCount(int degreesCount) {
		this.degreesCount = degreesCount;
	}
	public double getPromNrosem() {
		return promNrosem;
	}
	public void setPromNrosem(double promNrosem) {
		this.promNrosem = promNrosem;
	}
}
